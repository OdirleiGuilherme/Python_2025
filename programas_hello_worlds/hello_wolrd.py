print('Transformando um mensagem de minúsculo para tudo maiúsculo utilize (.upper)')
print('texto original: hello world')
print('texto com (.upper) aplicado: hello world'.upper())
print('Transformando um mensagem de minúsculo para tudo maiúsculo utilize (.lower)')
print('texto original: HELLO WORLD')
print('texto com (.lower) aplicado: hello world'.lower())

